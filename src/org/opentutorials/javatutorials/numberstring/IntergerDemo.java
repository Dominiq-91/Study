package org.opentutorials.javatutorials.numberstring;

public class IntergerDemo {

	public static void main(String[] args) {
		
		
		int a; //int를 사용함으로써 a안에 담길수 있는건 숫자뿐이다 라고 선언한것임.
		a = 1; //a라는 변수에 1이라는 숫자를 '대입(저장,할당,넣는다,초기화)'한것이다.
			System.out.println(a+1); //	출력값 = 2
			
		a = 2; //a를 1에서 2로 재설정 한것임.
			System.out.println(a+1); // 출력값 = 3 (a를 2로 재설정했기때문)
			
			
		double b; //double은 실수를 선언하는 데이터 타입이다.
		b  = 1.1;
			System.out.println(b+1.1);
			

	}

}
