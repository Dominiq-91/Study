package java1013;

public class Method {
	public static void numbering(int init, int limit) {  // numburing이라는 이름을 가지는 메소드를 정의
		int i = init;				// int limit = 매개변수(parameter)
		while ( i < limit) {
			System.out.print(i+" ");
			i++;
		}
		System.out.println();
	}
	public static void main(String[] args) { 
		
		numbering(3,5);  // numbering이라는 이름을 가지고 있는 메소드를 호출
				// 5 = 인자(argument)
		
		
		
		
/*
			참조형타입 : 기본형타입을 제외한 모든 타입.
			
			
*/
			int i = 4;   // 기본형타임
		
			String str = new String("hello");
			
			// String 클래스타입의 참조변수 str을 생성한다.
			
			
			
			
	}

}
