package java0915;

public class 기본형 {

	public static void main(String[] args) {
		
		boolean isFun = true;
		System.out.println(isFun);
		
		char c = 'f';
		
		int x = 59;
		
		long bing = 34545433L; // long 타입은 수치입력후 반드시 소문자 'l' or 대문자'L'을 입력후에 세미콜론으로 문장을 닫아야한다.
		
		float f = 32.4f; // 마찬가지로 f를 사용해 닫아준다.
		
		double d = 34343.5; 
				
		System.out.println(c);
		System.out.println(x);
		System.out.println(bing);
		System.out.println(f);
		System.out.println(d);
		
	}

}
